# Rsolucao do delta


a = int(input('Entre com o valor de a: '))
b = int(input('Entre com o valor de b: '))
c = int(input('Entre com o valor de c: '))

delta = ((b**2) - (4*a*c))

print('O valor de delta é:', delta)


